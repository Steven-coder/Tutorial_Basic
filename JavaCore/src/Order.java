
public class Order {
	private int Id;
	private int CustId;
	private String Description;
	private int Total;
	private String OrderDate;
	
	public int getId() {
		return Id;
	}
	public int getCustId() {
		return CustId;
	}
	public String getDescription() {
		return Description;
	}
	public int getTotal() {
		return Total;
	}
	public String getOrderDate() {
		return OrderDate;
	}
	
	
	
}
